package com.learnmaven.mavendemo;

public class Calculator {

	//calculator Add
	public int add(int a, int b )
	{return a+b;}

	//calculator Subtract
	public int Subtract(int a, int b )
	{return a-b;}

	//calculator Multiply
	public int Multi(int a, int b )
	{return a*b;}

	//calculator Divide
	public int div(int a, int b )
	{return a / b;}
	
}
